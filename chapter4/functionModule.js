function add(a,b){
	var sum = a + b;
	return sum;
}

function sub(a,b){
	var result = a - b;
	return result;
}

function mul(a,b){
	var result = a * b;
	return result;
}

function div(a,b){
	var result = a / b;
	return result;
}

exports.add = add;
exports.sub = sub;
exports.mul = mul;
exports.div = div;